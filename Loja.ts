export class Loja{
    nome: string
    autor: string
    categoria: string
    ano: number
    preco: number

    constructor(nome: string, autor: string, categoria: string, ano: number, preco: number) {
        this.nome = nome
        this.autor = nome
        this.categoria = categoria
        this.ano = ano
        this.preco = preco
    }
}